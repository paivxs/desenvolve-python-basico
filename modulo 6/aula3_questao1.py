# Exercício 1 
# lista de números

# lista com os valores fornecidos
valores = [2, 7, 15, 30, 51]

# mostra a lista original
print("Lista original: ", valores)

# mostra os 3 primeiros elementos da lista
print("3 primeiros elementos: ", valores[:3])

# mostra os dois ultimos elementos da lista
print("2 últimos elementos: ", valores[:-2])

#  mostra a lista invertida do fim para o começo
print("Lista invertida: ", valores[::-1])

# mostra os elementos de índice par
print("Elementos de índice par: ", valores[::2])
